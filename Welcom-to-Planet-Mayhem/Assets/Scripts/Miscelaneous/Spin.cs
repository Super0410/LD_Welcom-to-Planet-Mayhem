#region Imports

using UnityEngine;

#endregion

public class Spin : MonoBehaviour
{
	//===============================================================================================================================//
	//==================================================== Inspector Variables ======================================================//
	//===============================================================================================================================//

	#region Inspector Variables

	[SerializeField] Vector3 eulerAngles;

	#endregion

	//===============================================================================================================================//
	//==================================================== Unity Event Functions ====================================================//
	//===============================================================================================================================//

	#region Unity Event Functions

	void Update ()
	{
		transform.Rotate (eulerAngles * Time.deltaTime, Space.Self);
	}

	#endregion

}
